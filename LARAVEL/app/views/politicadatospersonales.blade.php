                <p>2. Cumplimiento de obligaciones laborales;</p>
                <p>3. Cumplimiento de obligaciones contractuales;</p>
                <p>4. Llevar a cabo proceso de negociaci&oacute;n;</p>
                <p>5. Ofrecimiento de productos y servicios.</p>

                <p>&nbsp;</p>
                <p>Una vez agotada la finalidad para la que los datos fueron recolectados se proceder&aacute; con su eliminaci&oacute;n. En caso de que el Titular haya solicitado la supresi&oacute;n del dato, se proceder&aacute; a lo propio, luego de agotado el tr&aacute;mite legal y siempre que no medie relaci&oacute;n contractual vigente entre el Titular y La Compa&ntilde;&iacute;a.</p>
