                <p><strong>POL&Iacute;TICAS DE TRATAMIENTO DE DATOS PERSONALES</strong></p>
                <p>El presente documento establece las Pol&iacute;ticas de Tratamiento de Datos Personales de la sociedad TORRECAFE AGUILA ROJA &amp; CIA S.A., (en adelante La Compa&ntilde;&iacute;a) De conformidad con la Ley 1581 de 2012, el Decreto 1377 de 2013 y dem&aacute;s normas que lo complementen, y en &eacute;l se describen los mecanismos por medio de los cuales la COMPA&Ntilde;&Iacute;A garantiza un manejo adecuado de los datos personales recolectados en sus bases de datos, permitiendo a los titulares el ejercicio del derecho de H&aacute;beas Data, teniendo en cuenta las siguientes:</p>
                <p>&nbsp;</p>
                <h2><strong>1. Definiciones:</strong></h2>
                    <p>1. <strong>Base de Datos :</strong> Conjunto organizado de datos personales que sea objeto de Tratamiento;</p>
                    <p>2. <strong>Dato personal:</strong> Cualquier informaci&oacute;n vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables;</p>
                    <p>3. <strong>Dato p&uacute;blico:</strong> Es el dato que no sea semiprivado, privado o sensible. Son considerados datos p&uacute;blicos, entre otros, los datos relativos al estado civil de las personas, a su profesi&oacute;n u oficio y a su calidad de comerciante o de servidor p&uacute;blico. Por su naturaleza, los datos p&uacute;blicos pueden estar contenidos, entre otros en registros p&uacute;blicos, documentos p&uacute;blicos, gacetas y boletines oficiales y sentencias judiciales debidamente ejecutoriadas que no est&eacute;n sometidos a reserva.</p>
                    <p>4. <strong>Encargado del Tratamiento:</strong> Persona natural o jur&iacute;dica, p&uacute;blica o privada, que por s&iacute; misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento;</p>
                    <p>5. <strong>Responsable del Tratamiento:</strong> Persona natural o jur&iacute;dica, p&uacute;blica o privada, que por s&iacute; misma o en asocio con otros, decida sobre la Base de Datos y/o el Tratamiento de los datos;</p>
                    <p>6. <strong>Titular:</strong> Persona natural cuyos datos personales sean objeto de Tratamiento;</p>
                    <p>7. <strong>Tratamiento:</strong> Cualquier operaci&oacute;n o conjunto de operaciones sobre datos personales, tales como la recolecci&oacute;n, almacenamiento, uso, circulaci&oacute;n o supresi&oacute;n;</p>
                <p>Tomado de la Ley 1581 de 2012 y del Decreto 1377 de 2013.
                <p>&nbsp;</p>
                <h2><strong>2. Consideraciones:</strong></h2>
                    <p>1. TORRECAFE AGUILA ROJA &amp; CIA S.A.,es una Compa&ntilde;&iacute;a comprometida con la protecci&oacute;n de datos personales.</p>
                    <p>2. La Pol&iacute;tica se aplicar&aacute; a todos los datos personales que trata o llegare a tratar La Compa&ntilde;&iacute;a.</p>
                    <p>3. La Pol&iacute;tica es de estricto cumplimiento para La Compa&ntilde;&iacute;a y para toda persona, natural o jur&iacute;dica, que en calidad de Encargado trate datos personales cuyo Responsable sea La Compa&ntilde;&iacute;a, por el t&eacute;rmino del Tratamiento, y aun cuando no subsistan v&iacute;nculos legales, contractuales, laborales o de cualquier otra &iacute;ndole.</p>
                    <p>4. Los Titulares de los datos personales de forma libre y voluntaria han suministrado la informaci&oacute;n.</p>
                    <p>5. Los Titulares de los datos personales se obligan a suministrar informaci&oacute;n veraz, completa, exacta, actualizada, comprobable y comprensible.</p>
                    <p>6. Cualquier cambio en La Pol&iacute;tica le ser&aacute; comunicado oportunamente a los Titulares de los datos personales antes de su implementaci&oacute;n.</p>
