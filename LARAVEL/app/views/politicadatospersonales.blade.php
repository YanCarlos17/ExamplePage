                <h2><strong>4. Tratamiento y finalidad de Datos personales</strong></h2>


                <p>TORRECAFE AGUILA ROJA &amp; CIA S.A., recolecta, almacena, usa, actualiza, circula y elimina los datos personales de sus clientes, proveedores y trabajadores para dar cumplimiento a las obligaciones legales y contractuales que le sean exigibles, as&iacute;, tambi&eacute;n, como herramienta en la etapa de negociaci&oacute;n y, en cumplimiento del principio de buena fe, en la etapa poscontractual.</p>
                <p>&nbsp;</p>
