        <p>* En total se tendrán 3 ganadores principales que serán los merecedores de la estufa, nevera y lavadora; y 50 más con anchetas de la marca. La mejor representación se llevará la nevera, el segundo lugar la lavadora y el tercer lugar la estufa. </p>
        
