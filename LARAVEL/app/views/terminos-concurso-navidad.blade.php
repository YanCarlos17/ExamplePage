        <p><strong>Pasos</strong></p>

        <p>- Las familias participantes deben representar de forma idéntica la escena indicada del pesebre que aparece en la fotografía publicada, teniendo en cuenta todos los elementos que ésta tiene y siendo creativos con buena estética.</p>

        <p>- Las familias deben tomar una foto de la representación que hicieron y subirla en el post indicado.</p>

        <p><strong>Ganarán las fotos que cumplan con las condiciones y sean más parecidas a la escena de nuestro comercial navideño.</strong></p>

