        <p>Para conmemorar la temporada navideña, Café Águila Roja plantea un concurso dirigido a hombres y mujeres mayores de edad, residentes en Colombia.</p>
        
        <p><strong>I. Medios de difusión:</strong></p>
        
        <p>La promoción del concurso será difundida a través de:</p>

        <p>Se promocionará en todas las redes sociales (Facebook e Instagram) de la marca a través de una publicación.</p>
        
        <p><strong>II. Participantes:</strong></p>

        <p>Podrán participar hombres y mujeres mayores de edad, residentes en Colombia, que cumplan con los requisitos descritos en la mecánica y que tengan un perfil activo y público en Facebook. Además, deberán aceptar los términos y condiciones aquí contenidos.</p>
        
        <p><strong>Quedan expresamente excluidos de participar en el concurso las siguientes personas:</strong></p>

        <p>Los empleados de Café Águila Roja, sus cónyuges y familiares hasta el segundo grado de consanguinidad, segundo de afinidad y único civil.</p>
        
        <p>Los empleados y trabajadores de las compañías proveedoras de insumos relacionados con los productos participantes, sus cónyuges y familiares hasta el segundo grado de consanguinidad, segundo de afinidad y único civil.</p>
        
        <p>Los empleados y trabajadores de las agencias de publicidad intervinientes en la actividad promocional, sus cónyuges y familiares hasta el segundo grado de consanguinidad, segundo de afinidad y único civil.</p>
        
        <p><strong>III. Mecánica promocional:</strong> </p>
        
        <p>El concurso “¡En navidad premia tu casa con Café Águila Roja!” se llevará a cabo en todo el territorio Colombiano durante 1 mes, a partir del 21 de noviembre al 18 de diciembre de 2017. Se escogerán los ganadores el 19 de diciembre de 2017.</p>
        
        <p>Para participar en la presente actividad promocional, los usuarios deberán: </p>
        
        <p>Ingresar al fan page de Café Águila Roja en Facebook <a href="https://www.facebook.com/CafeAguilaRoja/">https://www.facebook.com/CafeAguilaRoja/</a> y darle “me gusta” a la página.</p>
        
        <p>Se publicarán dos (2) post: uno con la mecánica de participación y otro donde cada familia participante deberá adjuntar la foto que requiere el concurso.</p>
        
        <p>La actividad es la siguiente:</p>
        
        <p>Representa de forma idéntica con tu familia la escena del pesebre de nuestro comercial navideño.</p>
        
