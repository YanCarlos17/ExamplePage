            <p>Los dos primeros retos cuentan con la característica de ser por cumplimiento y el último es de carácter          meritorio. </p>
            
            <p>La forma de inscripción y actividades son las siguientes:</p>
            
            <strong>INSCRIPCIÓN:</strong>

            
            <p>Para inscribirse, los participantes deberán publicar, en el post que corresponde a este paso, una foto de           ellos con un empaque de la marca (Café Águila Roja) y etiquetar a la persona con la que va a participar por         los Mugs para 2.</p>
            
            <strong>RETO 1: ¡Café de letras!</strong>
            
            <p><strong>Paso 1:</strong> Junto a tu pareja o amigo(a) encuentra las 10 palabras ocultas en el “Café de letras”; tú deberás           encontrar 5 y tu pareja o amigo(a) otras 5.</p>
            
            
            <p><strong>Paso 2:</strong> Cada uno deberá escribirlas como comentario debajo del post correspondiente a este reto y formar una            frase con ellas. Solo podrán agregar conectores, no palabras nuevas; ¡sumarán puntos las frases más creativas!</p>
            
            <p><strong>RETO 2: ¿Cuántos paquetes tienes?</strong></p>
            
            <p><strong>Paso 1:</strong> Tómate una foto creativa con la mayor cantidad de paquetes que tengas, tu compañero también deberá          hacer lo mismo.</p> 
            
            <p><strong>Paso 2:</strong> Cada uno deberá publicar la foto en la parte inferior de este post etiquetando a su amigo;  ¡sumarán            puntos las fotos más creativas!</p> 
            
            <p>*No se premiará por la cantidad de empaques que hayan en la foto.
            
            <p><strong>RETO 3:</strong> Tu dedicatoria en los “Mugs para 2”.</p>
            
            <p><strong>Paso 1:</strong> Los participantes deberán escribir el mensaje de dedicatoria que quieren para el  Mug de su pareja o            amigo(a).</p> 
            
            <p><strong>Paso 2:</strong> Cada uno deberá publicar la dedicatoria en la parte inferior del post correspondiente a este reto y             etiquetar a su amigo; ¡suma puntos la pareja que escriba una dedicatoria original para su compañero de          competencia!</p>
            
