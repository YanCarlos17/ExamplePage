               CONCURSO “¡SEAMOS AMIGOS EN AMOR Y AMISTAD!”             
            </strong>

            <p>Para conmemorar el mes del amor y la amistad, Café Águila Roja plantea un concurso dirigido a mujeres y hombres mayores de edad, residentes en Colombia.</p>
            
            <strong>I. Medios de difusión:</strong>
            
            <p>La promoción del concurso será difundida a través de:</p>

            <p>• Se promocionará en todas las redes sociales (Facebook e Instagram) de la marca a través de una publicación.</p>
            
            <strong>II. Participantes:</strong>

            <p>Podrán participar hombres y mujeres mayores de edad con residencia en Colombia, que cumplan con los          requisitos descritos en la mecánica de los retos y que tengan un perfil activo y público en Facebook. Además,           deberán aceptar los términos y condiciones aquí contenidos.</p>
            
            <p>Quedan expresamente excluidos de participar en el concurso las siguientes personas:</p>
            Los empleados de Café Águila Roja, sus cónyuges y familiares hasta el segundo grado de consanguinidad, segundo          de afinidad y único civil.</p>
            
            <p>Los empleados y trabajadores de las compañías proveedoras de insumos relacionados con los productos          participantes, sus cónyuges y familiares hasta el segundo grado de consanguinidad, segundo de afinidad y único          civil.</p>
            
            <p>Los empleados y trabajadores de las agencias de publicidad intervinientes en la actividad promocional, sus           cónyuges y familiares hasta el segundo grado de consanguinidad, segundo de afinidad y único civil.</p>
            
            <strong>III. Mecánica promocional:</strong> 
            
            <p>El concurso ¡SEAMOS AMIGOS EN AMOR Y AMISTAD!, se llevará a cabo en todo el territorio Colombiano durante            quince (15) días, a partir del 8 al 22 de septiembre de 2017. Finalizando el 25 de septiembre de 2017 a las         6:00 PM.</p>
            
            <p>Para participar en la presente actividad promocional, los usuarios deberán: </p>
            
            <p>Ingresar al fan page de Café Águila Roja en Facebook https://www.facebook.com/CafeAguilaRoja/ y darle “me            gusta” a la página.</p>
            
