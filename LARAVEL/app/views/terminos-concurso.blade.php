            <p>Se informará a los ganadores del concurso <strong>¡Seamos amigos en amor y amistad!:</strong></p>
            
            <p>· A través de mensaje interno en Facebook, solicitándoles el envío de sus datos personales (nombre completo,            documento de identidad, ciudad, dirección, teléfono y correo electrónico).</p>
            
            <p>· A través de mensajes privados se les confirmará a los ganadores que sus datos han sido recibidos.</p>
            
            <p>· A través de publicaciones en la página de Facebook anunciando que es uno de los ganadores.</p>
            
            <p>· Se contactará a los ganadores vía telefónica para la entrega del premio con un representante de Café Águila           Roja o un correo certificado el cual coordinará la fecha y hora de envío al lugar designado por el          participante en un lapso de 2 semanas o más según el lugar de residencia.</p>
            
            <p><strong>VI. Planilla de entrega de premios:</strong></p>
            
            <p>Todo ganador deberá firmar una planilla que certifica la entrega del premio y su recibo a satisfacción; además          deberá presentar su cédula de ciudadanía original. Sin el cumplimiento de la totalidad de lo anteriormente          descrito el ganador no podrá recibir su premio.</p>
            
